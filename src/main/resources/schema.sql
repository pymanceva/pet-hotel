CREATE TABLE IF NOT EXISTS pets
(
    id                 BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    owner_id_pet       INTEGER                                 NOT NULL,
    type_pet           VARCHAR(250)                            NOT NULL,
    name_pet           VARCHAR(30)                             NOT NULL,
    breed_pet          VARCHAR(30)                             NOT NULL,
    age_pet            VARCHAR(10)                             NOT NULL,
    sex_pet            VARCHAR(13)                             NOT NULL,
    CONSTRAINT pk_pet PRIMARY KEY (id)
--     CONSTRAINT fk_pets_to_users FOREIGN KEY (owner_id_pet) REFERENCES users (id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS rooms
(
    id        BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    number    VARCHAR(100)                            NOT NULL,
    price     REAL,
    size      REAL,
    type      VARCHAR(16)                             NOT NULL,
    available BOOLEAN DEFAULT TRUE,
    CONSTRAINT pk_room PRIMARY KEY (id)
);


